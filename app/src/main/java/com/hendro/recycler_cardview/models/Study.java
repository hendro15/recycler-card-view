package com.hendro.recycler_cardview.models;

public class Study {
    private String namaSekolah;
    private String kota;
    private int tahunMasuk;
    private int tahunLulus;
    private String jurusan;
    private String jenisSekolah;

    public String getNamaSekolah() {
        return namaSekolah;
    }

    public void setNamaSekolah(String namaSekolah) {
        this.namaSekolah = namaSekolah;
    }

    public String getKota() {
        return kota;
    }

    public void setKota(String kota) {
        this.kota = kota;
    }

    public int getTahunMasuk() {
        return tahunMasuk;
    }

    public void setTahunMasuk(int tahunMasuk) {
        this.tahunMasuk = tahunMasuk;
    }

    public int getTahunLulus() {
        return tahunLulus;
    }

    public void setTahunLulus(int tahunLulus) {
        this.tahunLulus = tahunLulus;
    }

    public String getJurusan() {
        return jurusan;
    }

    public void setJurusan(String jurusan) {
        this.jurusan = jurusan;
    }

    public String getJenisSekolah() {
        return jenisSekolah;
    }

    public void setJenisSekolah(String jenisSekolah) {
        this.jenisSekolah = jenisSekolah;
    }
}
